/**
 * add - Sums int
 * @a: First int
 * @b: 2nd int
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Gets dufference
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: diff
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Multiplies 2 int
 * @a: First int
 * @b: 2nd int
 *
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Gets quotient
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Gets remainder
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
